package calculator;

public class Calculator {

    public int addTwoNumbers(int a, int b) {
        return a+b;
    }

    public int subTwoNumbers(int a, int b) {
        return a-b;
    }

    public int multiplyTwoNumbers(int a, int b) {
        return a*b;
    }

    public int divTwoNumbers(int a, int b) {
        return a/b;
    }
}
